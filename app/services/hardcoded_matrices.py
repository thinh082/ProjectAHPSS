from fractions import Fraction


def parse_frac(s: str) -> float:
    """Chuyển string phân số ('1/3', '5/2', '3/2', '1') sang float."""
    s = s.strip()
    if '/' in s:
        num, den = s.split('/')
        return float(num) / float(den)
    return float(s)


def matrix_str_to_float(matrix_str: list[list[str]]) -> list[list[float]]:
    """Chuyển ma trận string sang ma trận float."""
    return [[parse_frac(v) for v in row] for row in matrix_str]


